int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	if (nb == 0)
		return (0);
	while ((i * i) < nb && nb < 46341)
		i++;
	if ((i * i) == nb)
		return (i);
	else
		return (0);
}
 int main()
 {
	printf("%i", ft_sqrt(2147483647));
	return (0);
 }
